    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            int m = Integer.parseInt(jTextField4.getText());
            int n = Integer.parseInt(jTextField7.getText());
            int o = n * m;
            jTextField13.setText(Integer.toString(o));

        } catch (NumberFormatException ex) {
            jTextField13.setText("error!");
        }
        try {
            int m = Integer.parseInt(jTextField2.getText());
            int n = Integer.parseInt(jTextField9.getText());
            int o = n * m;
            jTextField15.setText(Integer.toString(o));
        } catch (NumberFormatException ex) {
            jTextField15.setText("error!");
        }
        try {
            int m = Integer.parseInt(jTextField6.getText());
            int n = Integer.parseInt(jTextField8.getText());
            int o = n * m;
            jTextField14.setText(Integer.toString(o));
        } catch (NumberFormatException ex) {
            jTextField14.setText("error!");
        }
        try {
            int m = Integer.parseInt(jTextField17.getText());
            int n = Integer.parseInt(jTextField18.getText());
            int o = n * m;
            jTextField19.setText(Integer.toString(o));
        } catch (NumberFormatException ex) {
            jTextField19.setText("error!");
        }
        try {
            int m = Integer.parseInt(jTextField23.getText());
            int n = Integer.parseInt(jTextField20.getText());
            int o = n * m;
            jTextField21.setText(Integer.toString(o));
        } catch (NumberFormatException ex) {
            jTextField21.setText("error!");
        }
                //sum up all gp for each course
        int cgpSum = Integer.parseInt(jTextField21.getText())
                + Integer.parseInt(jTextField19.getText())
                + Integer.parseInt(jTextField14.getText())
                + Integer.parseInt(jTextField15.getText())
                + Integer.parseInt(jTextField13.getText());
                // sum up the total course unit
        int totalCu = Integer.parseInt(jTextField2.getText())
                + Integer.parseInt(jTextField4.getText())
                + Integer.parseInt(jTextField6.getText())
                + Integer.parseInt(jTextField17.getText())
                + Integer.parseInt(jTextField23.getText());
        
        double gpa = (double) cgpSum / totalCu;
        double roundGpa = Math.round(gpa * 100.0) / 100.0d;
        String outcome = Double.toString(roundGpa);

        try {
            jLabel2.setText("Your CGPA is " + outcome);
                //Status
            if (roundGpa >= 4.50) {
                jLabel3.setText("You are on First Class Honors");
            } else if (roundGpa >= 3.50 && roundGpa < 4.50) {
                jLabel3.setText("You are on Second Class Honours (Upper Division)");
            } else if (roundGpa >= 2.40 && roundGpa < 3.50) {
                jLabel3.setText("You are on Second Class Honours (Lower Division)");
            } else if (roundGpa >= 1.50 && roundGpa < 2.40) {
                jLabel3.setText("You are on Third Class Honours");
            } else if (roundGpa >= 1.00 && roundGpa < 1.50) {
                jLabel3.setText("You've got  Pass Degree");
            } else {
                jLabel3.setText("Status can't be generated at this time, try again");
            }
        } catch (NumberFormatException ex) {
            jLabel2.setText("Fill all fields");
            jLabel3.setText("Fill all fields");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

